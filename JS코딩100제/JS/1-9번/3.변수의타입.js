// 2)  string
//  - arr은 배열의 형태로 쓰였다.

// 3)  number
//  - arr은 배열의 형태로 쓰였다.

// 4)  object
//  - JS에선 number, string, boolean, null, undefined, symbol을 제외한 모든 타입은 object 타입을 가진다.

